package com.zego.rn.widget.loading;

import androidx.annotation.NonNull;

import com.facebook.react.uimanager.ThemedReactContext;
import com.facebook.react.uimanager.ViewGroupManager;

public class RNLoadingView extends ViewGroupManager<LoadingView> {

    private static final String COMMPONET_NAME = "RNLoadingView";
    @NonNull
    @Override
    protected LoadingView createViewInstance(@NonNull ThemedReactContext reactContext) {
        return new LoadingView(reactContext);
    }

    @NonNull
    @Override
    public String getName() {
        return COMMPONET_NAME;
    }
}
